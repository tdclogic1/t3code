export type SalesSkillCategory =
  | "Prospecting"
  | "Account Research"
  | "Outreach"
  | "Qualification"
  | "Discovery"
  | "Proposal & Deal Strategy"
  | "Deal Execution"
  | "Forecasting & Pipeline"
  | "Account Growth"
  | "RevOps & Enablement";

