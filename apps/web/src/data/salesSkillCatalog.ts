function createCategorySkills(
  category: SalesSkillCategory,
  skills: ReadonlyArray<SalesSkillSeed>,
): ReadonlyArray<SalesSkillBase> {
  return skills.map((skill) => ({ ...skill, category }));
}

function unique(values: ReadonlyArray<string>): ReadonlyArray<string> {
  return Array.from(new Set(values));
}

function createSlug(value: string): string {
  return value
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

